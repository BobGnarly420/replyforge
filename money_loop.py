def money_loop():
    print("🚀 ReplyForge MONEY LOOP started")
    print(f"Target: ${TARGET_REVENUE}")
    print(f"Looping every {SLEEP_SECONDS}s. Press Ctrl+C to stop.\n")

    total_revenue = 0.0
    cycle = 0

    while total_revenue < TARGET_REVENUE:
        cycle += 1
        print(f"\n--- CYCLE {cycle} | Current revenue: ${total_revenue} ---")

        for i in range(LEADS_PER_CYCLE):
            lead = generate_lead()
            log_to_notion(lead)
            create_notion_page_via_mcp(lead)

            # Simulate outreach happening
            if simulate_send_pitch(lead):
                print("   🔥 They replied interested!")
                updated = record_revenue(lead)
                total_revenue += updated["Revenue"]
                print(f"   💰 CLOSED ${updated['Revenue']}! Running total: ${total_revenue}")

                # Log the paid version too
                create_notion_page_via_mcp(updated)
            else:
                print("   → Pitched. Waiting for reply...")

        print(f"Cycle complete. Sleeping {SLEEP_SECONDS} seconds...\n")
        time.sleep(SLEEP_SECONDS)

    print(f"\n🎉 TARGET REACHED! ${total_revenue} logged.")
    print("Loop finished. Go scale it or raise the target.")


